public class DormitoryItem {
    private String dormitoryNum;
    private String dormitoryName;
    private String dormitoryText;
    private String imageUrl;

    public String getDormitoryNum() {
        return dormitoryNum;
    }

    public void setDormitoryNum(String dormitoryNum) {
        this.dormitoryNum = dormitoryNum;
    }

    public String getDormitoryName() {
        return dormitoryName;
    }

    public void setDormitoryName(String dormitoryName) {
        this.dormitoryName = dormitoryName;
    }

    public String getDormitoryText() {
        return dormitoryText;
    }

    public void setDormitoryText(String dormitoryText) {
        this.dormitoryText = dormitoryText;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }


}